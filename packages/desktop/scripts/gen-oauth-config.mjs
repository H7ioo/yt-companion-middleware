// @ts-check
// Build-time generator for the bundled OAuth client constants (PRD-03 §1.1, issue 011).
//
// Reads YT_BUNDLED_CLIENT_ID / YT_BUNDLED_CLIENT_SECRET from the environment (set in CI) and
// writes packages/desktop/generated/oauth.mjs — a gitignored constant the shipped binary carries
// but the repo never does. The secret of an unverified desktop client isn't truly secret; this
// just keeps it out of git history.
//
// Absent env => empty strings and HAS_BUNDLED_CLIENT=false, so a local dev build simply offers no
// bundled flow (override-only) and nothing crashes. See issues/011.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const defaultOutDir = path.resolve(here, "..", "generated"); // packages/desktop/generated

/**
 * Renders the generated oauth.mjs source for a client id/secret pair. Values are JSON-encoded so a
 * stray quote or backslash can't break out of the string literal. Missing values become "".
 * @param {{ clientId?: string, clientSecret?: string }} [creds]
 * @returns {string}
 */
export function renderOAuthConfig(creds = {}) {
  const id = creds.clientId ?? "";
  const secret = creds.clientSecret ?? "";
  return `// GENERATED by scripts/gen-oauth-config.mjs — do not edit, do not commit.
// Bundled OAuth client, injected at build time from CI env. Empty in local dev builds, where the
// app offers only the override credential flow (PRD-03 §1.1).
export const BUNDLED_CLIENT_ID = ${JSON.stringify(id)};
export const BUNDLED_CLIENT_SECRET = ${JSON.stringify(secret)};
export const HAS_BUNDLED_CLIENT = ${JSON.stringify(Boolean(id && secret))};
`;
}

/**
 * Writes generated/oauth.mjs from the given environment. Creates the directory if needed.
 * @param {NodeJS.ProcessEnv} [env]
 * @param {string} [outDir]
 * @returns {string} the path written
 */
export function writeOAuthConfig(env = process.env, outDir = defaultOutDir) {
  const source = renderOAuthConfig({
    clientId: env.YT_BUNDLED_CLIENT_ID,
    clientSecret: env.YT_BUNDLED_CLIENT_SECRET,
  });
  fs.mkdirSync(outDir, { recursive: true });
  const outFile = path.join(outDir, "oauth.mjs");
  fs.writeFileSync(outFile, source);
  return outFile;
}

// Run as a build step when invoked directly; stay inert when imported by tests.
if (process.argv[1] && import.meta.url === `file://${process.argv[1]}`) {
  const written = writeOAuthConfig();
  const present = Boolean(process.env.YT_BUNDLED_CLIENT_ID && process.env.YT_BUNDLED_CLIENT_SECRET);
  console.log(
    `[gen-oauth-config] wrote ${written} — bundled client ${present ? "present" : "absent (override-only build)"}`,
  );
}
